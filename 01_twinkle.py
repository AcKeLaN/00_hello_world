from time import sleep


def twinkle():
    print("Twinkle, twinkle little star")
    sleep(3)
    print("How I wonder what you are")
    sleep(3)


def up_above():
    print("Up above the world so high")
    sleep(3)
    print("Like a diamond in the sky")
    sleep(3)


twinkle()
up_above()
twinkle()
